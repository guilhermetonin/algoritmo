'''
percorre uma lista existente
começa no primeiro parâmetro e vai até o último - 1
'''

valores = [5, 10, 15, 20, 25]
for x in valores:
  print(x)

nomes = ['fer', 'vitor', 29, -19]
for n in nomes:
  print(n)

for y in range(5):
  print("Valor: ", y)
# de 0 a 4

for a in range(5,10):
  print("Valor: ", a)
# de 5 a 9

for b in range(2,10,2):
  print("Valor: ", b)
# 2, 4, 6, 8